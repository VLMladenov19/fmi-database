-----MOVIES-----
USE movies

SELECT
	ADDRESS
FROM STUDIO
WHERE NAME = 'Disney'

SELECT
	BIRTHDATE
FROM MOVIESTAR
WHERE NAME = 'Jack Nicholson'

SELECT
	STARNAME
FROM STARSIN
WHERE MOVIEYEAR = 1980 OR MOVIETITLE LIKE '%Knight%'

SELECT
	NAME
FROM MOVIEEXEC
WHERE NETWORTH >= 10000000

SELECT
	NAME
FROM MOVIESTAR
WHERE GENDER = 'M' OR ADDRESS = 'Prefect Rd.'


-----PC-----
USE pc

SELECT 
	model,
	speed AS MHZ,
	hd AS GB
FROM PC
WHERE price < 1200

SELECT
	DISTINCT(maker)
FROM product
WHERE type = 'Printer'

SELECT
	model,
	ram,
	screen
FROM laptop
WHERE price > 1000

SELECT
	*
FROM printer
WHERE color = 'y'

SELECT
	model,
	ram, 
	hd
FROM pc
WHERE (cd = '12x' OR cd = '16x') AND price < 2000


-----SHIPS-----
USE ships

SELECT
	CLASS,
	COUNTRY
FROM CLASSES
WHERE NUMGUNS < 10

SELECT
	NAME AS SHIPNAMES
FROM SHIPS
WHERE LAUNCHED < 1918

SELECT
	SHIP,
	BATTLE
FROM OUTCOMES
WHERE RESULT = 'sunk'

SELECT
	NAME
FROM SHIPS
WHERE NAME = CLASS

SELECT
	NAME
FROM SHIPS
WHERE NAME LIKE 'R%'

SELECT
	NAME
FROM SHIPS
WHERE NAME LIKE '% %'

SELECT
	NAME
FROM SHIPS
WHERE NAME LIKE '% %' AND NAME NOT LIKE '% % %'
